# ── CELL 7 │ Layer Scan → 감정별 최적 Steering Layer ────────────
@torch.no_grad()
def steer_single(prompt, direction, steer_layer, alpha, max_new_tokens=40):
    dir_t = torch.tensor(direction, dtype=torch.bfloat16).to(model.device)
    def hook_fn(module, inp, output):
        res = output[0] if isinstance(output, tuple) else output
        return (res + alpha * dir_t,) + output[1:] if isinstance(output, tuple) \
               else res + alpha * dir_t
    handle = model.model.layers[steer_layer].register_forward_hook(hook_fn)
    try:
        inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
        out = model.generate(**inputs, max_new_tokens=max_new_tokens,
                              do_sample=True, temperature=0.7,
                              pad_token_id=tokenizer.eos_token_id)
        return tokenizer.decode(out[0][inputs["input_ids"].shape[1]:],
                                skip_special_tokens=True)
    finally:
        handle.remove()

@torch.no_grad()
def all_emotion_scores(text, layer_idx):
    inputs = tokenizer(text, return_tensors="pt",
                       truncation=True, max_length=128).to(model.device)
    out = model(**inputs, output_hidden_states=True, return_dict=True)
    h   = out.hidden_states[layer_idx][0, -1, :].float().cpu().numpy()
    return {emo: float(np.dot(h, directions[emo])) for emo in EMOTIONS}

