@torch.no_grad()
def extract_hidden_states(prompts, desc=""):
    results = []
    for prompt in tqdm(prompts, desc=desc, leave=False):
        inputs = tokenizer(prompt, return_tensors="pt",
                           truncation=True, max_length=128).to(model.device)
        out = model(**inputs, output_hidden_states=True, return_dict=True)
        last_tok = torch.stack(
            [h[0, -1, :].float().cpu() for h in out.hidden_states]
        )
        results.append(last_tok.numpy())
    return np.stack(results)   # (n_prompts, n_layers+1, hidden_dim)

