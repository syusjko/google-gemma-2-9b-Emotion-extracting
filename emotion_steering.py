# ── CELL 5 │ 방향 벡터 추출 + 극성 교정 ─────────────────────────
def compute_directions(emotion_acts, layer_idx):
    """Difference-in-Means: d_e = normalize(mean_e - global_mean)"""
    all_vecs     = np.concatenate([a[:, layer_idx, :] for a in emotion_acts.values()], axis=0)
    global_mean  = all_vecs.mean(axis=0)
    directions   = {}
    for emo, acts in emotion_acts.items():
        diff = acts[:, layer_idx, :].mean(axis=0) - global_mean
        directions[emo] = diff / (np.linalg.norm(diff) + 1e-8)
    return directions

